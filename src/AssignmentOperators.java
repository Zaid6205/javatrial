public class AssignmentOperators {
    public static void main(String[] args) {

        int m1,m2,m3,m4;
        m1=m2=m3=m4=10;
        System.out.println(m1 + " " +m2 + " " +m3 + " " +m4);

        // example 2
        int a =10;
        a+=5;
        System.out.println(a);
        int b=10;
        b-=5;
        System.out.println(b);
        int c =100;
        c/=10;
        System.out.println(c);
        int f =11;
        f*=11;
        System.out.println(f);
        int g =12;
        g%=4;
        System.out.println(g);

    }
}
